dir
#(Windows) / ls (Linux/Unix): Ambos os comandos listam o conteúdo de um diretório (pasta). Eles mostram os arquivos e subdiretórios presentes no diretório atual.

cls 
#(Windows) / clear (Linux/Unix): Esses comandos limpam a tela do terminal, removendo o histórico de comandos e resultados anteriores. Eles não afetam os arquivos ou diretórios no sistema de arquivos.

pwd 
#(Linux/Unix): Este comando imprime o diretório de trabalho atual (presente). Ele exibe o caminho completo do diretório em que você está atualmente no sistema de arquivos.

cd \: 
#Esse comando muda o diretório atual para a raiz do sistema de arquivos. No Windows, a raiz é indicada pelo caractere \, enquanto no Linux/Unix é indicada pelo caractere /.

exit: 
#Esse comando encerra a sessão atual do terminal. Ele fecha a janela do terminal ou encerra a sessão de linha de comando, dependendo do ambiente em que está sendo executado.

#Estes são comandos básicos encontrados em sistemas operacionais Windows e Unix/Linux que
#são frequentemente usados para navegar no sistema de arquivos, gerenciar arquivos e controlar sessões de terminal.
