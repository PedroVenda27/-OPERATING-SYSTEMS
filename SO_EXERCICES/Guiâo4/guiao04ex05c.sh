#O comando ping é uma ferramenta de diagnóstico utilizada para verificar a conectividade entre dois dispositivos em uma rede IP.
#Ele envia pacotes ICMP (Internet Control Message Protocol) para o dispositivo de destino e aguarda por respostas.
#O comando é bastante útil para verificar se um determinado host está acessível na rede e também para medir o tempo de resposta entre o host de origem e o destino.

#A sintaxe básica do comando ping é:

ping [opções] destino
#Algumas opções comuns do comando ping incluem:

-c count: Especifica o número de pacotes a serem enviados.
-i interval: Especifica o intervalo entre os pacotes enviados.
-s packetsize: Especifica o tamanho do pacote a ser enviado.
-t timeout: Especifica o tempo limite para esperar por uma resposta.
-v: Aumenta a verbosidade, exibindo mais informações.
-n: Usa endereços IP em vez de nomes de host.
#Para verificar se é possível contatar um site específico, você pode simplesmente usar o comando ping seguido do nome de domínio do site ou seu endereço IP.
