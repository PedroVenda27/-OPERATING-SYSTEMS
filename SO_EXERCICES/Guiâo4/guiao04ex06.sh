# é uma função em PowerShell chamada Get-PwShell-Version que tem a finalidade de retornar a versão do PowerShell utilizada.
# A função utiliza a propriedade PSVersion da tabela $PSVersionTable para obter essa informação.

#Quando você chama a função Get-PwShell-Version, ela simplesmente retorna a versão do PowerShell atualmente em execução.
